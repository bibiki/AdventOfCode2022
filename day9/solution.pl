trace_move(move(Direction, 0), Head, Head, Tail, Tail, Trace, Trace).
trace_move(move(Direction, Steps), Head, H, Tail, Tl, Trace, Tr) :-
    S is Steps - 1,
    next_head(Direction, Head, NextHead),
    next_tail(NextHead, Tail, NextTail),
    add_trace(NextTail, Trace, NextTrace),
    trace_move(move(Direction, S), NextHead, H, NextTail, Tl, NextTrace, Tr).

make_moves([], Head, Tail, Trace, Trace).
