print_array([]).
print_array([X|Xs]) :- write(X), nl, print_array(Xs).

abs(X, X) :- X >= 0.
abs(X, Y) :- X < 0, Y is -1 * X.

next_tail([Hx, Hy], [Tx, Ty], [Tx, Ty]) :-
    Xdiff is Hx - Tx,
    Ydiff is Hy - Ty,
    abs(Xdiff, X), abs(Ydiff, Y),
    X =< 1, Y =< 1.

next_tail([Hx, Hy], [Tx, Hy], [Tx1, Hy]) :- Hx > Tx, Tx1 is Tx + 1.
next_tail([Hx, Hy], [Tx, Hy], [Tx1, Hy]) :- Hx < Tx, Tx1 is Tx - 1.

next_tail([Hx, Hy], [Hx, Ty], [Hx, Ty1]) :- Hy > Ty, Ty1 is Ty + 1.
next_tail([Hx, Hy], [Hx, Ty], [Hx, Ty1]) :- Hy < Ty, Ty1 is Ty - 1.

next_tail([Hx, Hy], [Tx, Ty], [Tx1, Ty1]) :- Hx > Tx, Hy > Ty, Tx1 is Tx + 1, Ty1 is Ty + 1.
next_tail([Hx, Hy], [Tx, Ty], [Tx1, Ty1]) :- Hx < Tx, Hy < Ty, Tx1 is Tx - 1, Ty1 is Ty - 1.

next_tail([Hx, Hy], [Tx, Ty], [Tx1, Ty1]) :- Hx > Tx, Hy < Ty, Tx1 is Tx + 1, Ty1 is Ty - 1.
next_tail([Hx, Hy], [Tx, Ty], [Tx1, Ty1]) :- Hx < Tx, Hy > Ty, Tx1 is Tx - 1, Ty1 is Ty + 1.

next_head(r, [Hx, Hy], [Hx1, Hy]) :- Hx1 is Hx + 1.
next_head(l, [Hx, Hy], [Hx1, Hy]) :- Hx1 is Hx - 1.
next_head(d, [Hx, Hy], [Hx, Hy1]) :- Hy1 is Hy - 1.
next_head(u, [Hx, Hy], [Hx, Hy1]) :- Hy1 is Hy + 1.

add_trace(Tail, Trace, Trace) :- member(Tail, Trace).
